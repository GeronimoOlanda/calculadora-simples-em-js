let valor1 = 20;
let valor2 = 30;



let soma = function(valor1,valor2){
     somar = valor1+valor2;
     return somar;
}

let menos = function(valor1,valor2){
    m = valor1-valor2;

    return m;
}

let mult = function(valor1,valor2){
    m = valor1*valor2;

    return m;
}

let div = function(valor1,valor2){
    di = valor1/valor2;

    return di;
}


console.log(soma(20,39))
console.log(menos(20,39))
